DROP TABLE MCD_MENU;

CREATE TABLE MCD_MENU(
    WAITING_NAME VARCHAR2(20),
    MENU_NO NUMBER,
    MENU_NAME VARCHAR2(20) NOT NULL,
    PRICE NUMBER NOT NULL,
    RESTAURANT_CODE NUMBER NOT NULL,
    FOOD CHAR(3) CHECK(FOOD IN ('Y','N')),
    STARSCORE NUMBER,
    PRIMARY KEY (WAITING_NAME, MENU_NO)
);

COMMENT ON COLUMN MCD_MENU.WAITING_NAME IS '대기자';
COMMENT ON COLUMN MCD_MENU.MENU_NO IS '메뉴번호';
COMMENT ON COLUMN MCD_MENU.MENU_NAME IS '메뉴이름';
COMMENT ON COLUMN MCD_MENU.PRICE IS '가격';
COMMENT ON COLUMN MCD_MENU.RESTAURANT_CODE IS '매장코드';
COMMENT ON COLUMN MCD_MENU.FOOD IS '음식여부';
COMMENT ON COLUMN MCD_MENU.STARSCORE IS '음식평점';

