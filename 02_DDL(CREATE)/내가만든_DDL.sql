CREATE TABLE SURVEY(
    SURVEY_CODE NUMBER,
    RESTAURANT_CODE NUMBER,
    SERVICE_LEVEL NUMBER NOT NULL,
    CLEAN_SCORE NUMBER,
    IMPROVEMENT_POINTS VARCHAR2(50),
    SURVEY_WRITER VARCHAR2(20),
     PRIMARY KEY (SURVEY_CODE, RESTAURANT_CODE)
);

COMMENT ON COLUMN SURVEY.SURVEY_CODE IS '작성자번호';
COMMENT ON COLUMN SURVEY.RESTAURANT_CODE IS '매장코드';
COMMENT ON COLUMN SURVEY.SERVICE_LEVEL IS '서비스만족도';
COMMENT ON COLUMN SURVEY.CLEAN_SCORE IS '청결도';
COMMENT ON COLUMN SURVEY.IMPROVEMENT_POINTS IS '개선점';
COMMENT ON COLUMN SURVEY.SURVEY_WRITER IS '작성자';

